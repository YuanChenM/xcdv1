package com.msk.core.consts;

/**
 * The const defines for String.
 */
public interface StringConst {

    /** empty */
    String EMPTY = "";

    /** blank */
    String BLANK = " ";

    /** SINGLE_QUOTE */
    String QUOTE = "\"";

    /** char：. */
    String DOT = ".";

    /** char：, */
    String COMMA = ",";

    /** Semicolon ; */
    String SEMICOLON = ";";

    /** Exclamation. */
    String EXCLAMATION = "!";

    /** COLON - ":". */
    String COLON = ":";

    /** EQUATE - "=". */
    String EQUATE = "=";

    /** Middle_Line - '-' */
    String MIDDLE_LINE = "-";

    /** fuzzy condition character - * */
    String STAR = "*";

    /** % */
    String PRE = "%";

    /** Left bracket */
    String LEFT_BRACKET = "(";

    /** Right bracket */
    String RIGHT_BRACKET = ")";

    /** Left bracket */
    String LEFT_BRACE = "[";

    /** Right bracket */
    String RIGHT_BRACE = "]";

    /** Right bracket */
    String LEFT_BRACE_B = "{";

    /** Right bracket */
    String RIGHT_BRACE_B = "}";

    /** _ */
    String UNDERLINE = "_";

    /** | */
    String VERTICAL = "|";

    /** \n. */
    String NEWLINE = "\n";

    /** ALL */
    String ALL = "all";
}
